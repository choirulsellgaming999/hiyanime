function displayImages(imageList) {
    const imageContainer = document.getElementById('videoContainer');
    imageContainer.innerHTML = '';
    imageList.forEach(image => {
        const imageElement = document.createElement('div');
        imageElement.className = 'image-container';
        imageElement.innerHTML = `
            <a href="${image.link}" class="image-link">
                <img class="image-thumbnail" src="${image.src}" alt="${image.title}">
                <h3>${image.title}</h3>
            </a>
        `;
        imageContainer.appendChild(imageElement);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const shuffledImages = shuffle([...images]);
    displayImages(shuffledImages);
    observeImages();
});

function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

function observeImages() {
    const images = document.querySelectorAll('.image-thumbnail');

    const options = {
        root: null,
        rootMargin: '0px',
        threshold: 0.5
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            const image = entry.target;
            if (entry.isIntersecting) {
                setTimeout(() => {
                    if (entry.isIntersecting) {
                        image.style.opacity = 1; 
                    }
                }, 4000); 
            } else {
                image.style.opacity = 0; 
            }
        });
    }, options);

    images.forEach(image => {
        image.style.opacity = 0; 
        observer.observe(image);
    });
}
